def render_rop(input_text: str) -> str:
    return CANONICAL_ROP_TEMPLATE.replace("[INPUT]", input_text.strip() or "[INPUT]") + "\n"


def rop_payload(input_text: str) -> dict[str, Any]:
    rendered = render_rop(input_text)
    return {
        "schema": ROP_VERSION,
        "required_sections": list(REQUIRED_ROP_SECTIONS),
        "text": rendered,
        "stop_rule": "STOP_IF[EXPECTED_IMPROVEMENT < COMPLEXITY_COST]",
    }


def validate_rop_text(text: str) -> list[str]:
    upper = text.upper()
    missing = [section for section in REQUIRED_ROP_SECTIONS if section not in upper]
    if "STOP_IF[" not in upper or "EXPECTED_IMPROVEMENT" not in upper or "COMPLEXITY_COST" not in upper:
        missing.append("STOP_IF[EXPECTED_IMPROVEMENT < COMPLEXITY_COST]")
    return missing
